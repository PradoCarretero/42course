#include"libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ch;
	int				i;

	ch = (unsigned char *) b;
	i = 0;
	while (i < (int)len)
	{
		ch[i] = c;
		i++;
	}
	return (ch);
}
